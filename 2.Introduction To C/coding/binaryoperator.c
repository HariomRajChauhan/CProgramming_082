#include<stdio.h>

int main(){
    // binary operator

    int a = 10;
    int b = 20;

    int sum = a+b;
    int sub = a-b;
    int mul = a*b;
    float div = a/b;

    printf("sum = %d\n", sum);
    printf("sub = %d\n", sub);
    printf("mul = %d\n", mul);
    printf("div = %f\n", div);
    return 0;
}